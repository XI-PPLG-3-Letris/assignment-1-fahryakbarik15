        // Operator : Simbolik untuk melakukan sebuah operasi di dalam pemrograman

        /*
        Operator Aritmatika
        Operator Penugasan
        Operator Logika
        Operator Perbandingan
        Operator Ternary
        */

        // 1.Operator Aritmatika
        /*
        Penjumlahan (+)
        Pengurangan (-)
        Perkalian (*)
        Pembagian (/)
        Perpangkatan (**)
        Sisa Bagi / Modulus (%)
        */

        let a = 10;
        let b = 2;

// 1. Penjumlahan
console.log("Penjumlahan:", a + b);

// 2. Pengurangan
console.log("Pengurangan:", a - b);

// 3. Perkalian
console.log("Perkalian:", a * b);

// 4. Pembagian
console.log("Pembagian:", a / b);

// 5. Perpangkatan
console.log("Perpangkatan:", a ** b);

// 6. Sisa Bagi / Modulus
console.log("Modulus:", a % b);